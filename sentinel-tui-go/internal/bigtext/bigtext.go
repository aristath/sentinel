func renderWith(s string, height int, lookup func(rune) ([7]string, bool)) string {
	lines := [7]strings.Builder{}

	for _, r := range s {
		if r >= 'a' && r <= 'z' {
			r = r - 'a' + 'A'
		}
		g, ok := lookup(r)
		if !ok {
			sp, _ := lookup(' ')
			g = sp
		}
		for i := 0; i < height; i++ {
			lines[i].WriteString(g[i])
		}
	}

	parts := make([]string, height)
	for i := 0; i < height; i++ {
		parts[i] = lines[i].String()
	}
	return strings.Join(parts, "\n")
}
